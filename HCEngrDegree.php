<span class="home_subtitles">Process Improvement in Healthcare Short Course</span>
<p>The <a href="http://mapp.usc.edu/professionalprograms/ShortCourses/ProcessImprovementinHealthCare.htm" target="_blank">Process Improvement in Health Care short course</a> combines the field of Industrial and Systems Engineering with the needs of the health care industry. The National Academy of Engineering found that an inefficient American health care system causes 100,000 preventable deaths and the unnecessary loss of $500 billion each year.* In order to reduce costs and improve patient care, course participants will learn methods to improve efficiency in health care processes.</p>

<p>Useful, practical and popular process improvement methods will be explained and students will have an opportunity to implement many of them, including:</p>
<ul>
    <li>Process Mapping - how to understand a process with the use of diagrams and graphics</li>
    <li>Queuing - understanding models of processes and their analysis</li>
    <li>Materials management - inventory, its control and optimizing</li>
    <li>Six Sigma basics - how this popular approach is used to reduce unwanted variability and improve quality</li>
    <li>Lean thinking basics - how this popular approach is used to eliminate waste</li>
    <li>Facility layout - why do some arrangements harm productivity and others help it</li>
    <li>Computer simulation - how to make use of this valuable tool</li>
    <li>Focus Groups - how to organize them and how to make them effective</li>
    <li>Change management - once the necessary improvements are identified, how to implement</li>
</ul>
